public class IterarArreglo {

    public static void main(String[] args) {
        //Sintaxis simplificada
        int[] enteros = {100, 200, 300, 400, 500};
        
        for(int i=0; i < enteros.length; i++){
            System.out.println("Valor del indice es " + i + " de " +enteros[i]);
        }
    }
    
}
