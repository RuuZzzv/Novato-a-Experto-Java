
public class CicloFor {

    public static void main(String[] args) {

        //Imprimir del 1 al 5
        var contador = 1;
        final var VALOR_MAXIMO = 5;
        //ciclo for
        for (contador = 1; contador <= VALOR_MAXIMO; contador++) {
            System.out.println("Valor: " + contador);
        }
    }
}
